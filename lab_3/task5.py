# Програма рахує кількість секунд у заданій кількості годин

hours = 2  # Кількість годин
seconds_in_hour = 3600  # Кількість секунд у годині

print("Кількість годин:", hours)  # Виведення кількості годин
print("Кількість секунд у годині:", hours * seconds_in_hour)  # Виведення кількості секунд

# Виведення завершального повідомлення
print("Програма завершена.")
